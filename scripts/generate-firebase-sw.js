/**
 * Generates firebase-messaging-sw.js with Firebase config from environment variables.
 * Service workers cannot access process.env at runtime, so we inject config at build time.
 * Run before: next build (already in package.json)
 *
 * Required env vars (set in Vercel / .env.local):
 *   NEXT_PUBLIC_FIREBASE_API_KEY
 *   NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN
 *   NEXT_PUBLIC_FIREBASE_PROJECT_ID
 *   NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET
 *   NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID
 *   NEXT_PUBLIC_FIREBASE_APP_ID
 */

const fs = require('fs');
const path = require('path');

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY || '',
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN || '',
  databaseURL: process.env.NEXT_PUBLIC_FIREBASE_DATABASE_URL || '',
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID || '',
  storageBucket: process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET || '',
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID || '',
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID || '',
  measurementId: process.env.NEXT_PUBLIC_FIREBASE_MEASUREMENT_ID || '',
};

const template = `// Firebase Cloud Messaging Service Worker (generated at build - do not edit manually)
// Generated by scripts/generate-firebase-sw.js

importScripts("https://www.gstatic.com/firebasejs/10.9.0/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/10.9.0/firebase-messaging-compat.js");

const firebaseConfig = ${JSON.stringify(config, null, 2)};

firebase.initializeApp(firebaseConfig);
const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const notificationTitle = payload.notification?.title || payload.data?.title || "YooKatale";
  const notificationBody = payload.notification?.body || payload.data?.body || "You have a new notification";
  const iconUrl = "https://www.yookatale.app/assets/icons/logo2.png";
  const badgeUrl = "https://www.yookatale.app/assets/icons/logo2.png";
  const notificationUrl = payload.data?.url || payload.data?.click_action || payload.fcmOptions?.link || payload.webpush?.fcmOptions?.link || "https://www.yookatale.app/schedule";

  const notificationOptions = {
    body: notificationBody,
    icon: iconUrl,
    badge: badgeUrl,
    image: payload.notification?.image || payload.data?.image || undefined,
    tag: payload.data?.mealType || payload.data?.tag || "yookatale-notification",
    requireInteraction: false,
    vibrate: [200, 100, 200],
    timestamp: Date.now(),
    silent: false,
    renotify: true,
    data: {
      url: notificationUrl,
      mealType: payload.data?.mealType || '',
      click_action: notificationUrl,
      title: notificationTitle,
      body: notificationBody,
    },
    actions: [
      { action: 'open', title: 'View Schedule', icon: iconUrl },
      { action: 'close', title: 'Close' },
    ],
  };

  return self.registration.showNotification(notificationTitle, notificationOptions).catch((error) => {
    console.error("[SW] Error showing notification:", error);
    return self.registration.showNotification(notificationTitle, {
      body: notificationBody,
      icon: iconUrl,
      data: { url: notificationUrl }
    });
  });
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();
  event.waitUntil(
    clients.matchAll({ type: "window", includeUncontrolled: true })
      .then((clientList) => {
        for (let i = 0; i < clientList.length; i++) {
          const client = clientList[i];
          if (client.url === "/" && "focus" in client) return client.focus();
        }
        if (clients.openWindow) {
          const url = event.notification.data?.url || event.notification.data?.click_action || "https://www.yookatale.app/";
          return clients.openWindow(url);
        }
      })
  );
});
`;

const outPath = path.join(__dirname, '..', 'public', 'firebase-messaging-sw.js');
fs.writeFileSync(outPath, template, 'utf8');

const hasRequired = config.apiKey && config.messagingSenderId && config.appId;
if (!hasRequired) {
  console.warn('⚠️  Firebase config incomplete. Push notifications will not work until you set:');
  console.warn('    NEXT_PUBLIC_FIREBASE_API_KEY');
  console.warn('    NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID');
  console.warn('    NEXT_PUBLIC_FIREBASE_APP_ID');
  console.warn('    (and other NEXT_PUBLIC_FIREBASE_* vars)');
} else {
  console.log('✅ firebase-messaging-sw.js generated with Firebase config');
}
